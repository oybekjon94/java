package javaBasics;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
        //The main is a method. A method is a piece of code created to do a specific job.
        //This static method can be called without the need to create an instance of the Java class.
        //The void keyword states that the method does not return a value.
        //the public keyword makes the main method available to the outer world without restrictions.
    }
}
